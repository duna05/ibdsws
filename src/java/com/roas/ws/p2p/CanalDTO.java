package com.roas.ws.p2p;

public class CanalDTO {

	private String codCanal;
	private String usuarioCanal;
	private String clvCanal;
	public String getCodCanal() {
		return codCanal;
	}
	public void setCodCanal(String codCanal) {
		this.codCanal = codCanal;
	}
	public String getUsuarioCanal() {
		return usuarioCanal;
	}
	public void setUsuarioCanal(String usuarioCanal) {
		this.usuarioCanal = usuarioCanal;
	}
	public String getClvCanal() {
		return clvCanal;
	}
	public void setClvCanal(String clvCanal) {
		this.clvCanal = clvCanal;
	}
	
}
